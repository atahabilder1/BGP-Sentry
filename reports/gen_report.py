parts.append(r"""
\textbf{Likely root causes:}

\begin{enumerate}
\item \textbf{Vote timeout ($30$s) too tight for 778-validator consensus propagation.} At \texttt{caida\_800} the timeout of $20$s with 410 RPKI validators worked; doubling the validator count more than doubles the mean time for $\sqrt{N}=27$ vote responses to return.
\item \textbf{Cold KB at high $N$.} The warm-up phase ($300$s) populates per-validator knowledge but in a 778-validator network the knowledge takes longer to propagate to quorum ($\lceil \log N \rceil \approx 10$ gossip hops). Early transactions vote \texttt{no\_knowledge} before KB is seeded.
\item \textbf{Broadcast size $\sqrt{N}=27$ may be slightly under-provisioned} for $N=778$: asking 27 peers and needing 5 approves works only if $P(\text{approve}) \geq 0.35$, which is marginal at cold start.
\end{enumerate}

\textbf{Recommended fixes for the next run:}
\begin{itemize}
\item Raise \texttt{P2P\_REGULAR\_TIMEOUT} from 30s to 45--60s in \texttt{.env.1600}.
\item Extend \texttt{WARMUP\_DURATION} from 300s to 600s for \texttt{caida\_1600}.
\item Increase broadcast floor: change formula to \texttt{max(threshold$\times$3, $\sqrt{N}$)} so at least 15 peers are asked.
\end{itemize}

\subsection{Issue 2 --- Recall attenuation on static detectors at scale}

\texttt{PREFIX\_HIJACK} recall at \texttt{caida\_1600} is 55.0\% versus
90.6\% at \texttt{caida\_800}. Similar pattern on \texttt{SUBPREFIX\_HIJACK}
(52.2\% at 1600). This is \emph{not} a detector failure --- precision
stays at 99.8\%/100\%. The drop is driven by the \texttt{RPKI\_DEDUP\_WINDOW}
(15s): after the first observation of a given (prefix, origin) is flagged
within the window, subsequent observations are marked \texttt{skipped\_dedup}
and counted as FN against recall.

\textbf{Fix:} either extend the dedup window to $0$ during recall
computation, or report recall at the \emph{event} level rather than
the \emph{observation} level (the latter is more consistent with how
production BGP monitors are measured).

\subsection{Issue 3 --- System-level performance\_metrics reports 0/0}

\texttt{results/caida\_N/.../performance\_metrics.json} reports
\texttt{precision=0, recall=0} on every run. This disagrees with the
per-type precision/recall we compute from
\texttt{detection\_results.json}. Reason: that file is computed against
\emph{attack consensus verdicts} (multi-validator votes that an attack
occurred) rather than per-observation detector fires. The verdict
consensus evidently fails to match ground-truth events in this run.

\textbf{Recommended diagnostic:} audit
\texttt{attack\_consensus.py} verdict-to-ground-truth matching logic. The
detector-level numbers in §\ref{sec:detection} are authoritative for
the paper.

\subsection{Issue 4 --- Fork rate scales super-linearly with $N$}
""")
