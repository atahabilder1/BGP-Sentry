# ── helpers ────────────────────────────────────────────────────────────
def pct(n, d, digits=1):
    if d == 0 or d is None:
        return "—"
    return f"{100*n/d:.{digits}f}\\%"

def fmt_int(n):
    if n is None:
        return "—"
    return f"{int(n):,}"

def fmt_float(n, digits=2):
    if n is None:
        return "—"
    return f"{n:.{digits}f}"

def sizes_sorted():
    return sorted(D.keys(), key=lambda k: int(k.replace("caida_","")))

def tex_name(s):
    """LaTeX-safe dataset name (escape underscore)."""
    return s.replace("_", "\\_")

